    os.makedirs("./predictions", exist_ok=True)
    onsite_pred_filename = f"task3_{backbone}{attn_suffix}_onsite_kaggle.csv"
    ids = pd.read_csv(onsite_test_csv)['id']
    onsite_prediction = pd.DataFrame(onsite_preds, columns=['D', 'G', 'A'])
