    print(f"✓ Saved ONSITE predictions: ./predictions/{onsite_pred_filename}")

    offsite_pred_filename = f"task3_{backbone}{attn_suffix}_offsite_results.csv"
    ids_offsite = pd.read_csv(offsite_test_csv)['id']
